
/*

    변수 (variable) - 변하는 값
    상수 (constant) - 변하지 않는 값 

    숫자가 원래 3이였는데 숫자2를 더하면 값이 5 (변수)
    3 + 2 = 5 
    5
    연산된 결과의 값을 어떤 공간에 저장하고 이름을 지어준 후 언제든 재사용

    어떠한 연산 값을 컴퓨터가 기억하게 함
    그 다음 해당 값이 필요할때 언제든 재사용 할 수 있는 것

    console.log(3)
    console.log(3+2)
    console.log(3+2+2)

    let a = 3;
    console,log(a)

    a = 3 + 2;
    console.log(a)

    a = a + 2;
    console.log(a)

    => 변수/상수는 재사용을 위해 컴퓨터에게 값을 기억시키는 것

    let a = 5;
    a = 5 + 7;

    let(변하기위해 )

    const b =3;
    b = 7 (x);
    -> 변하지않을때,변해서는 안됨 재사용을 위해 컴퓨토에 값을 기억시킨다)

    const(수정하면 안됨 / 한번 쓸때 )
*/

//let은 최초 처음만 사용가능
let a = 5;
console.log(a)
//let --> 선언문 
// 내가 이 변수를 처음으로 컴퓨터에게 기억시켰다라고 선언


a = 7;
// 초기화, 재할당 
console .log(a)